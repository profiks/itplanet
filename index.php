  <h2> Что приносят в бюро находок?  </h2>
   </div> 
<div class=block_green>
  
<p> Стол находок — это место, куда могут принести, в принципе, любые вещи. Например, вы можете представить себе человека, который забыл инвалидную коляску (в которой и передвигался) или собственные костыли? Просто представьте: он ведь действительно придёт за ними в бюро находок и обязательно расскажет, как потерял.
В Японии, например, в стол находок относят самые разные вещи, в том числе — клочки бумаги: вдруг на них важные телефонные номера или ещё что-то? Просто представьте: путешествуя по Токио вы выбрасываете бумажку из кармана — и этим же вечером она попадает в местное бюро находок где терпеливо вас ждёт несколько недель.
<p>Один из самых популярных предметов — это, без сомнения, зонты. Причём во многих странах дешевые зонтики без металлических элементов уже принято выбрасывать вместо того, чтобы нести в бюро находок — их действительно очень и очень много. Часто в стол находок пытаются отдать брошенных животных разных размеров — от хомячков до собак. В регионах России в бюро находок очень часто попадают паспорта и другие документы. В Иркутске, например, за два месяца собрали 300 паспортов и одно европейское водительское удостоверение гражданки Италии.
<p>Иногда в бюро находок попадают и очень необычные вещи — от забытых работающих цветных принтеров до «эха войны» — неразорвавшихся снарядов (их не берут, но время от времени приносят) или даже старинных телефонов. Часто доставляются сотовые телефоны, различные мелочи и многое-многое другое. На Востоке приносят даже деньги, в том числе – мелкие, то есть заведомо никому не нужные.
<p> Одним словом, если у вас что-то потерялось — не спешите расстраиваться. Вполне возможно, вы сможете найти это уже на следующий день в бюро находок, куда добросердечные граждане или работники городских служб доставят ваш предмет. </p>

